package git;

public class GitTheory {

//    DESPRE GIT
//    Git este un sistem distribuit de control al versiunilor
//    -este folosit pentru a urmari si a gestiona toate modificarile facute in fisierele unui proiect
//    - este folosit in special pt codul sursa dar poate fi folosit pentru orice fisier
//    - salveaza istoricul modificarilor din proiect, permite revenirea la orice vbersiune anterioara a unui fisier
//    - permite lucrul atat individual cat si colaborativ
//    - este o unealta locala

//    DESPRE GITHUB
//    GitHub-ul este o platforma online care gazduieste proiecte care folosesc Git
//    - permite colaborarea cu alti dezvoltatori indiferent unde se afla ei
//    - este o platforma online

//    TERMENI DE BZA IN GIT
//    Repository( Repo) = proiectul tau gestionat cu GIT
//    Commit= salvarea unui set de modificari cu un mesaj explicativ
//    Branch= o ramura prin care poti dezvolta functionalitati fara a afecta codul principal
//    Merge= combinarea modificarilor dintr-o ramura in alta
//    Push= trimiterea modificarilor locale catre un repository remote( GIT LOCAL -> GIT HUB REMOTE)
//    Pull= descarcarea ultimelor modificari de pe remote pe local ( GIT HUB REMOTE -> GIT LOCAL)

}
